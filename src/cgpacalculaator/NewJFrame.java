    jButton3.addActionListener(new java.awt.event.ActionListener() {
        public void actionPerformed(java.awt.event.ActionEvent evt) {
            String title = jTextField1.getText();
            String code = jTextField2.getText();
            String creditStr = jTextField3.getText();
            String letterGrade = jComboBox1.getSelectedItem().toString();

            if (title.isEmpty() || code.isEmpty() || creditStr.isEmpty() || letterGrade.equals("--select Letter Grade")) {
                javax.swing.JOptionPane.showMessageDialog(null, "Please fill in all fields.");
                return;
            }

            double credit = Double.parseDouble(creditStr);
            double gradePoint = getGradePoint(letterGrade);
            int rowNum = model.getRowCount() + 1;

            model.addRow(new Object[]{rowNum, title, code, credit, letterGrade, gradePoint});

            // Clear inputs
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jComboBox1.setSelectedIndex(0);
        }
    });
    jButton1.addActionListener(new java.awt.event.ActionListener() {
    public void actionPerformed(java.awt.event.ActionEvent evt) {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        int rowCount = model.getRowCount();

        double totalCredit = 0;
        double totalGradePoint = 0;

        for (int i = 0; i < rowCount; i++) {
            Object creditObj = model.getValueAt(i, 3);
            Object gpObj = model.getValueAt(i, 5);

            if (creditObj != null && gpObj != null) {
                double credit = Double.parseDouble(creditObj.toString());
                double gp = Double.parseDouble(gpObj.toString());

                totalCredit += credit;
                totalGradePoint += (credit * gp);
            }
        }

        double cgpa = totalCredit > 0 ? totalGradePoint / totalCredit : 0;

        jTextField4.setText(String.format("%.2f", totalCredit));
        jTextField5.setText(String.format("%.2f", totalGradePoint));
        jTextField6.setText(String.format("%.2f", cgpa));
    }
});
